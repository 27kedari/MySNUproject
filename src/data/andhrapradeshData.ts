export const searchLocationsInCity = (cityName: string, query: string): Location[] => {
  const cityData = getCityData(cityName);
  if (!cityData) return [];
  
  return cityData.locations.filter(location =>
    location.name.toLowerCase().includes(query.toLowerCase()) ||
    location.description.toLowerCase().includes(query.toLowerCase()) ||
    location.category.toLowerCase().includes(query.toLowerCase()) ||
    location.address.toLowerCase().includes(query.toLowerCase())
  );
};

export const searchAllLocations = (query: string): (Location & { city: string })[] => {
  const allResults: (Location & { city: string })[] = [];
  
  Object.entries(andhrapradeshData).forEach(([cityName, cityData]) => {
    const cityResults = searchLocationsInCity(cityName, query);
    cityResults.forEach(location => {
      allResults.push({ ...location, city: cityName });
    });
  });
  
  return allResults;
};

export const searchCities = (query: string): string[] => {
  return Object.keys(andhrapradeshData).filter(cityName =>
    cityName.toLowerCase().includes(query.toLowerCase())
  );
};

export const getAllCities = (): string[] => {
  return Object.keys(andhrapradeshData);
};

export const getCityData = (cityName: string): CityData | null => {
  return andhrapradeshData[cityName] || null;
};
